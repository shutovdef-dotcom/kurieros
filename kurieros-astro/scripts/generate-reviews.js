function getRandomCity(jobLocation) {
  if (jobLocation.toLowerCase() === "вся россия" || jobLocation.toLowerCase() === "крупные города рф") {
    return DEFAULT_CITIES[Math.floor(Math.random() * DEFAULT_CITIES.length)];
  }
  const cities = jobLocation.split(",").map(c => c.trim());
  return cities[Math.floor(Math.random() * cities.length)];
}

function getRandomAndPop(arr) {
  if (arr.length === 0) return "Нет данных";
  const index = Math.floor(Math.random() * arr.length);
  return arr.splice(index, 1)[0];
}

function main() {
  if (!fs.existsSync(JOBS_FILE)) {
    console.error("jobs.json not found!");
    return;
  }

  const jobsData = JSON.parse(fs.readFileSync(JOBS_FILE, 'utf8'));
  let reviewsData = [];
  let newReviewsCount = 0;

  jobsData.forEach(job => {
    const numReviewsToGenerate = Math.floor(Math.random() * 3) + 3; // 3 to 5 reviews
    
    // Create clones of arrays specifically for this job to prevent inner duplicates
    const availablePros = [...PROS_DB];
    const availableCons = [...CONS_DB];
    const availableComments = [...COMMENTS_DB];
    
    const availableProsErr = [...PROS_ERRORS_DB];
    const availableConsErr = [...CONS_ERRORS_DB];
    const availableCommentsErr = [...COMMENTS_ERRORS_DB];

    for (let i = 0; i < numReviewsToGenerate; i++) {
        const isErrorProne = Math.random() < 0.25; 
        
        // Pop ensures no duplicates in the same job
        const pros = isErrorProne ? getRandomAndPop(availableProsErr) : getRandomAndPop(availablePros);
        const cons = isErrorProne ? getRandomAndPop(availableConsErr) : getRandomAndPop(availableCons);
        const comment = isErrorProne ? getRandomAndPop(availableCommentsErr) : getRandomAndPop(availableComments);

        const ratings = [3.5, 3.8, 4.0, 4.2, 4.5, 4.8, 5.0];
        let rating = ratings[Math.floor(Math.random() * ratings.length)];
        if (isErrorProne && Math.random() > 0.5) rating = 3.5; 

        reviewsData.push({
            id: Date.now() + Math.floor(Math.random() * 1000000), 
            jobId: job.id,
            company: job.company, 
            jobTitle: job.title,  
            name: NAMES[Math.floor(Math.random() * NAMES.length)],
            city: getRandomCity(job.location),
            pros: pros,
            cons: cons,
            comment: comment,
            rating: rating,
            date: new Date(Date.now() - Math.floor(Math.random() * 10000000000)).toISOString() 
        });
        newReviewsCount++;
    }
  });

  if (newReviewsCount > 0) {
    fs.writeFileSync(REVIEWS_FILE, JSON.stringify(reviewsData, null, 2));
    console.log(`Generated ${newReviewsCount} unique reviews mapped strictly per vacancy.`);
  }
}

main();
